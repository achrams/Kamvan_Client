$(document).ready(() => {
    Auth()

})

function loginPage() {
    $('.signup').hide()
    $('.signin').hide()
    $('#navbarDropdownMenuLink').hide()
    $('#logoutbtn').hide()
}

function homePage() {
    $('.signup').hide()
    $('.signin').hide()
    $('.fixed-form').hide()
}

function registerPage() {
    $('.signup').show()
    $('.signin').hide()
    $('#navbarDropdownMenuLink').hide()
    $('#logoutbtn').hide()
}

function addForm() {
    $('.fixed-form').show()
}

function cancelbtn() {
    $('.fixed-form').hide()
}

function Auth() {
    if (localStorage.access_token) {
        homePage()
    } else {
        loginPage()
    }
}

$('#logoutbtn').click(() => {
    localStorage.clear()
    Auth()
})